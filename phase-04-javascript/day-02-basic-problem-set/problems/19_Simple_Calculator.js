let number1 = 3829338692379393;
let number2 = 82489903389;
let operator = "*";

switch (operator) {
    case "/":
        console.log(number1 + number2);
        break;

    case "-":
        console.log(number1 - number2);
        break;

    case "*":
        console.log(number1 * number2);
        break;

    case "/":
        console.log(number1 / number2);
        break;

    default:
        console.log("Invalid operator");
}